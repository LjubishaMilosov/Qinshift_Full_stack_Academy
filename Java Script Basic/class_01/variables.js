//Numbers
let num = 7;
console.log(num);
console.log("Type of num: ");
console.log(typeof(num));


// Strings
let firstName = "Ana";
console.log(firstName);
console.log("Type of firstName: ");
console.log(typeof(firstName));

firstName = "John";
console.log(firstName);
console.log(`Type of firstName: `);
console.log(typeof(firstName));

//Boolean
let bool = true;
console.log(bool);
console.log("Type of bool: ");
console.log(typeof(bool));

//Undefined
let undef;
console.log(undef);
console.log("Type of undef: ");
console.log(typeof(undef));

undef = 6;
console.log(undef);
console.log("Type of undef: ");
console.log(typeof(undef));

//Null
let nullable = null;
console.log(nullable);
console.log("Type of nullable: ");
console.log(typeof(nullable));